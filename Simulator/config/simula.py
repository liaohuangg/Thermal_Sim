def check_layout_overlap(layout_file_path):
    """
    æ£€æŸ¥å¸ƒå±€æ–‡ä»¶ä¸­å„å•å…ƒæ˜¯å¦å­˜åœ¨ä½ç½®é‡å 
    
    å‚æ•°ï¼š
        layout_file_path (str): å¸ƒå±€æ–‡ä»¶çš„ç»å¯¹è·¯å¾„æˆ–ç›¸å¯¹è·¯å¾„ï¼ˆå¦‚ "./chip_layout.flp"ï¼‰
    
    è¿”å›ï¼š
        tuple: (is_overlap, overlap_pairs)
            - is_overlap (bool): æ•´ä½“æ˜¯å¦å­˜åœ¨é‡å ï¼ˆTrue=æœ‰é‡å ï¼ŒFalse=æ— é‡å ï¼‰
            - overlap_pairs (list): é‡å çš„å•å…ƒå¯¹åˆ—è¡¨ï¼Œæ¯ä¸ªå…ƒç´ ä¸º (å•å…ƒ1åç§°, å•å…ƒ2åç§°)ï¼Œæ— é‡å æ—¶ä¸ºç©ºåˆ—è¡¨
    
    å¼‚å¸¸ï¼š
        FileNotFoundError: è‹¥è¾“å…¥çš„æ–‡ä»¶è·¯å¾„ä¸å­˜åœ¨
        ValueError: è‹¥æ–‡ä»¶å†…å®¹æ ¼å¼ä¸ç¬¦åˆè¦æ±‚ï¼ˆå¦‚ç¼ºå°‘å¿…è¦å­—æ®µï¼‰
    """
    # -------------------------- 1. è¯»å–å¹¶è§£æå¸ƒå±€æ–‡ä»¶ --------------------------
    units = {}  # å­˜å‚¨æ¯ä¸ªå•å…ƒçš„è¾¹ç•Œä¿¡æ¯ï¼škey=å•å…ƒåï¼Œvalue=å­—å…¸ï¼ˆleft_x, right_x, bottom_y, top_yï¼‰
    
    try:
        with open(layout_file_path, "r", encoding="utf-8") as f:
            lines = f.readlines()
    except FileNotFoundError:
        raise FileNotFoundError(f"å¸ƒå±€æ–‡ä»¶ä¸å­˜åœ¨ï¼Œè¯·æ£€æŸ¥è·¯å¾„ï¼š{layout_file_path}")
    
    for line_num, line in enumerate(lines, start=1):  # line_num ç”¨äºå®šä½é”™è¯¯è¡Œ
        line = line.strip()
        # è·³è¿‡æ³¨é‡Šè¡Œå’Œç©ºè¡Œ
        if not line or line.startswith("#"):
            continue
        
        # è§£ææ¯è¡Œå†…å®¹ï¼ˆæ ¼å¼è¦æ±‚ï¼š<unit-name> <width> <height> <left-x> <bottom-y>ï¼‰
        parts = line.split()
        if len(parts) != 5:
            raise ValueError(
                f"æ–‡ä»¶ç¬¬ {line_num} è¡Œæ ¼å¼é”™è¯¯ï¼ˆéœ€5ä¸ªå­—æ®µï¼‰ï¼Œå†…å®¹ï¼š{line}\n"
                "æ­£ç¡®æ ¼å¼ï¼š<unit-name> <width> <height> <left-x> <bottom-y>"
            )
        
        # æå–å­—æ®µå¹¶è½¬æ¢ä¸ºæ•°å€¼ï¼ˆå¤„ç†ç±»å‹é”™è¯¯ï¼‰
        try:
            unit_name = parts[0]
            width = float(parts[1])
            height = float(parts[2])
            left_x = float(parts[3])
            bottom_y = float(parts[4])
        except ValueError:
            raise ValueError(
                f"æ–‡ä»¶ç¬¬ {line_num} è¡Œæ•°å€¼æ ¼å¼é”™è¯¯ï¼Œå†…å®¹ï¼š{line}\n"
                "width/height/left-x/bottom-y éœ€ä¸ºæ•°å­—ï¼ˆå¦‚ 0.100000ï¼‰"
            )
        
        # è®¡ç®—å•å…ƒçš„å³è¾¹ç•Œå’Œä¸Šè¾¹ç•Œ
        right_x = left_x + width
        top_y = bottom_y + height
        
        # æ£€æŸ¥å•å…ƒåæ˜¯å¦é‡å¤ï¼ˆé¿å…é‡å¤è§£æè¦†ç›–ï¼‰
        if unit_name in units:
            raise ValueError(f"æ–‡ä»¶ä¸­å­˜åœ¨é‡å¤å•å…ƒåï¼š{unit_name}ï¼ˆç¬¬ {line_num} è¡Œï¼‰")
        
        units[unit_name] = {
            "left_x": left_x,
            "right_x": right_x,
            "bottom_y": bottom_y,
            "top_y": top_y
        }
    
    # è‹¥æ–‡ä»¶ä¸­æ— æœ‰æ•ˆå•å…ƒï¼ˆä»…æ³¨é‡Š/ç©ºè¡Œï¼‰ï¼Œè¿”å›æ— é‡å 
    if not units:
        print("è­¦å‘Šï¼šå¸ƒå±€æ–‡ä»¶ä¸­æœªè§£æåˆ°ä»»ä½•æœ‰æ•ˆå•å…ƒï¼ˆä»…æ³¨é‡Šæˆ–ç©ºè¡Œï¼‰")
        return (False, [])
    
    # -------------------------- 2. åˆ¤æ–­å•å…ƒé—´æ˜¯å¦é‡å  --------------------------
    def _is_two_units_overlap(unit1_bounds, unit2_bounds):
        """å†…éƒ¨å‡½æ•°ï¼šåˆ¤æ–­ä¸¤ä¸ªå•å…ƒçš„è¾¹ç•Œæ˜¯å¦é‡å ï¼ˆåˆ†ç¦»è½´å®šç†ï¼‰"""
        # è‹¥xè½´æ— äº¤é›† æˆ– yè½´æ— äº¤é›†ï¼Œåˆ™ä¸é‡å 
        x_no_overlap = (unit1_bounds["right_x"] <= unit2_bounds["left_x"]) or \
                       (unit1_bounds["left_x"] >= unit2_bounds["right_x"])
        y_no_overlap = (unit1_bounds["top_y"] <= unit2_bounds["bottom_y"]) or \
                       (unit1_bounds["bottom_y"] >= unit2_bounds["top_y"])
